#!/usr/bin/env python3
"""Export trained network weights and normalization stats as C arrays.

This script merges the same feature TSVs used for training, computes the
means and standard deviations exactly the way ``FeatureDataset`` does, and
then loads a saved PyTorch checkpoint to extract the weight and bias tensors.
All of these values are written to a header file ``model_weights.h`` which
can be included by a C program performing inference.

Usage::

    python export_model_c.py <checkpoint.pt> > model_weights.h

By default the script picks the first checkpoint found if none is
specified.  The generated header defines constants for input/hidden sizes
and arrays for means, stds, weights and biases.
"""

import sys
import glob
import pandas as pd
import numpy as np
import torch

# same feature file list as elsewhere
feature_files = [
    'extract/age/age.tsv',
    'extract/age_delta/age_delta.tsv',
    'extract/height_delta/height_delta.tsv',
    'extract/reach_delta/reach_delta.tsv',
    'extract/weight_delta/weight_delta.tsv',
    'extract/takedown_defense_delta/takedown_defense_delta.tsv',
    'extract/takedown_avg_delta/takedown_avg_delta.tsv',
    'extract/takedown_accuracy_delta/takedown_accuracy_delta.tsv',
    'extract/striking_advantage/striking_advantage.tsv',
    'extract/prev_wins_against_opp/prev_wins_against_opp.tsv',
    'extract/grappling_score/grappling_score.tsv',
]

def merge_all(scale_map=None):
    dfs = [pd.read_csv(f, sep='\t') for f in feature_files]
    df = dfs[0]
    for other in dfs[1:]:
        on = ['r_fighter', 'b_fighter', 'winner']
        if 'weight_class' in df.columns and 'weight_class' in other.columns:
            on.append('weight_class')
        new_cols = [c for c in other.columns if c not in df.columns or c in on]
        df = df.merge(other[new_cols], on=on, how='inner')
    if 'weight_diff' in df.columns and 'weight_delta' not in df.columns:
        df = df.rename(columns={'weight_diff': 'weight_delta'})
    if scale_map:
        for name,val in scale_map.items():
            if name in df.columns:
                df[name] = df[name] * val
            else:
                print(f'warning: scale column {name} not present')
    return df

# compute normalization stats
print('/* Auto-generated by export_model_c.py */')
print('#ifndef MODEL_WEIGHTS_H')
print('#define MODEL_WEIGHTS_H')
print()

import argparse

parser = argparse.ArgumentParser(description='Export network weights to C')
parser.add_argument('checkpoint', nargs='?',
                    help='checkpoint file to export (default: first found)')
parser.add_argument('--scale', action='append',
                    help='scale features prior to computing normalization (name=multiplier)')
args = parser.parse_args()

if args.checkpoint:
    ckpt = args.checkpoint
else:
    candidates = glob.glob('model/checkpoints/*_e*.pt')
    if not candidates:
        sys.exit('no checkpoints found')
    ckpt = candidates[0]

scale_map = {}
if args.scale:
    for spec in args.scale:
        try:
            name,val = spec.split('=',1)
            scale_map[name] = float(val)
        except Exception:
            print(f'warning: could not parse scale spec "{spec}"')

# load dataset to compute mean/std
print('// merging feature files to compute normalization parameters')
df = merge_all()
base = {'r_fighter','b_fighter','winner','weight_class'}
features = [c for c in df.columns if c not in base]
clean = df.dropna(subset=features).reset_index(drop=True)
arr = clean[features].values.astype(float)
mean = arr.mean(axis=0, keepdims=True)[0]
std = arr.std(axis=0, keepdims=True)[0]
std[std == 0] = 1.0
# clipping bounds for normalization (same as FeatureDataset)
print(f'#define MODEL_INPUT_DIM {len(features)}')
print(f'static const char *model_feature_names[{len(features)}] = {{')
for f in features:
    print(f'    "{f}",')
print('};')
print()
print(f'static const float model_mean[{len(features)}] = {{')
for v in mean:
    print(f'    {v:.6e}f,')
print('};')
print(f'static const float model_std[{len(features)}] = {{')
for v in std:
    print(f'    {v:.6e}f,')
print('};')
print()

# load checkpoint and dump weights
sd = torch.load(ckpt)
# determine layer sizes from tensors
w0 = sd['net.0.weight'].numpy()
b0 = sd['net.0.bias'].numpy()
w1 = sd['net.2.weight'].numpy()
b1 = sd['net.2.bias'].numpy()
# final layer may be net.4 or net.6 depending on hidden3; assume 4
w2 = sd['net.4.weight'].numpy()
b2 = sd['net.4.bias'].numpy()

h1 = w0.shape[0]
inh = w0.shape[1]
h2 = w1.shape[0]

print(f'#define MODEL_HIDDEN1 {h1}')
print(f'#define MODEL_HIDDEN2 {h2}')
print()
print(f'static const float model_w0[{h1}][{inh}] = {{')
for row in w0:
    print('    { ' + ', '.join(f'{x:.6e}f' for x in row) + ' },')
print('};')
print(f'static const float model_b0[{h1}] = {{')
for v in b0:
    print(f'    {v:.6e}f,')
print('};')
print()
print(f'static const float model_w1[{h2}][{h1}] = {{')
for row in w1:
    print('    { ' + ', '.join(f'{x:.6e}f' for x in row) + ' },')
print('};')
print(f'static const float model_b1[{h2}] = {{')
for v in b1:
    print(f'    {v:.6e}f,')
print('};')
print()
print(f'static const float model_w2[1][{h2}] = {{')
for row in w2:
    print('    { ' + ', '.join(f'{x:.6e}f' for x in row) + ' },')
print('};')
print(f'static const float model_b2[1] = {{ {b2[0]:.6e}f }};')

print()
print('#endif /* MODEL_WEIGHTS_H */')
